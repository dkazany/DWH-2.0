MERGE DWH.dbo.CUSTOMER_CONT_DATA AS TARGET
USING 
(SELECT a.ID, b.CONT_TYPE, b.CONT_DATA FROM DWH.dbo.CUSTOMER a, STG.dbo.CB_CUSTOMER_CONT_DATA b
WHERE a.CORE_BANKING_ID = b.CUST_SRC_SYS_ID) AS SOURCE
ON TARGET.CUSTOMER_ID = SOURCE.ID AND TARGET.CONT_DATA = SOURCE.CONT_DATA
WHEN MATCHED AND SOURCE.CONT_DATA <> TARGET.CONT_DATA THEN UPDATE 
SET TARGET.CONT_DATA = SOURCE.CONT_DATA
WHEN NOT MATCHED BY TARGET THEN
INSERT (CUSTOMER_ID, CONT_TYPE_ID, CONT_DATA)
VALUES 
(ID, CONT_TYPE, CONT_DATA);